public class PointDonkey {
    private float dx;

    private float dy;

    public PointDonkey(float dx, float dy) {
        this.dx = dx;
        this.dy = dy;
    }

    public float getDx() {
        return dx;
    }

    public void setDx(float dx) {
        this.dx = dx;
    }

    public float getDy() {
        return dy;
    }

    public void setDy(float dy) {
        this.dy = dy;
    }
}
